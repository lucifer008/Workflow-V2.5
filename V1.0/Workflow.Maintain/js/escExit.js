document.onkeydown=function(event_e)
{
	if (window.event)
		event_e=window.event;
	var int_keycode=event_e.charCode || event_e.keyCode;
	//alert(int_keycode);	
		
	if (int_keycode==27)	
	{
		window.close();
	}
}
